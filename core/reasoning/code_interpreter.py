class TorchCodeInterpreter(CodeInterpreter):
    """Code interpreter with PyTorch integration."""

    def __init__(self, config: CodeInterpreterConfig = None):
        if config is None:
            config = CodeInterpreterConfig()

        # Add numpy and torch to allowed modules for math operations
        config.allowed_modules = list(config.allowed_modules) + ['numpy']

        super().__init__(config)

    def execute_with_tensor_support(
        self,
        code: str,
        tensors: Dict[str, torch.Tensor] = None,
    ) -> ExecutionResult:
        """
        Execute code with tensor variables available.

        Args:
            code: Python code
            tensors: Dictionary of tensor variables to make available

        Returns:
            ExecutionResult
        """
        # Add tensor assignments to code
        if tensors:
            tensor_setup = []
            for name, tensor in tensors.items():
                # Convert to list for safe transmission
                tensor_setup.append(f"{name} = {tensor.tolist()}")
            setup_code = "\n".join(tensor_setup) + "\n"
            code = setup_code + code

        return self.execute(code)
